package tw.ebank.model;

import java.util.*;

public class NTTrade_model {
	private String outAcc;
	private String inAcc;
	private String balance;
	private String amount;
	private String other;
	private String date;
	public String getOutAcc() {
		return outAcc;
	}
	public void setOutAcc(String outAcc) {
		this.outAcc = outAcc;
	}
	public String getInAcc() {
		return inAcc;
	}
	public void setInAcc(String inAcc) {
		this.inAcc = inAcc;
	}
	public String getBalance() {
		return balance;
	}
	public void setBalance(String balance) {
		this.balance = balance;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getOther() {
		return other;
	}
	public void setOther(String other) {
		this.other = other;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
